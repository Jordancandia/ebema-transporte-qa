// Buscar la región a la que pertenece una comuna (comparación insensible a mayúsculas/acentos básicos)
export function findRegionByComuna(comuna) {
  if (!comuna) return '';
  const norm = String(comuna).trim().toLowerCase();
  for (const region of REGIONES) {
    const comunas = COMUNAS_POR_REGION[region] || [];
    if (comunas.some(c => c.toLowerCase() === norm)) return region;
  }
  return '';
}
